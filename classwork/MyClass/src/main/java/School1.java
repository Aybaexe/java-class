public class School1 {
    private String name;
    private String description;
    private int numberOfClassrooms;
    private double squareFootage;
    private int studenCapacity;
    private Cafeteria cafeterial;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public int getNumberOfClassrooms() {
        return numberOfClassrooms;
    }

    public void setNumberOfClassrooms(int numberOfClassrooms) {
        this.numberOfClassrooms = numberOfClassrooms;
    }

    public double getSquareFootage() {
        return squareFootage;
    }

    public void setSquareFootage(double squareFootage) {
        this.squareFootage = squareFootage;
    }

    public int getStudenCapacity() {
        return studenCapacity;
    }

    public void setStudenCapacity(int studenCapacity) {
        this.studenCapacity = studenCapacity;
    }

    public Cafeteria getCafeterial() {
        return cafeterial;
    }

    public void setCafeterial(Cafeteria cafeterial) {
        this.cafeterial = cafeterial;
    }
    
    
    
}
